using LinearAlgebra

function ks_hamiltonian(dzeta, zeta, params, t)
    mu = params[1]
    C = params[2]
    u = zeta[1:4]
    w = zeta[5:8]

    r = u[1]^2 + u[2]^2 + u[3]^2 + u[4]^2
    rE = sqrt((u[1]^2 - u[2]^2 - u[3]^2 + u[4]^2 + 1)^2 + 4*(u[1]*u[3] + u[2]*u[4])^2 + (2u[1]*u[2] - 2u[3]*u[4])^2)

    dzeta[1] = (1/4)*w[1] + (1/2)*(mu - 1 - u[1]^2 + u[2]^2 + u[3]^2 - u[4]^2) * u[2] + u[1]*u[3]*u[4]
    dzeta[2] = (1/4)*w[2] + (1/2)*(mu - 1 - u[1]^2 - u[2]^2 + u[3]^2 - u[4]^2) * u[1] + u[2]*u[3]*u[4]
    dzeta[3] = (1/4)*w[3] + (1/2)*(1 - mu + u[1]^2 - u[2]^2 + u[3]^2 + u[4]^2) * u[4] - u[1]*u[2]*u[3]
    dzeta[4] = (1/4)*w[4] - (1/2)*(mu - 1 - u[1]^2 + u[2]^2 + u[3]^2 + u[4]^2) * u[3] + u[1]*u[2]*u[4]

    dzeta[5] =  -C*u[1] 
                - (1/2)*w[2]*(mu - 1 - 3*u[1]^2 - u[2]^2 + u[3]^2 - u[4]^2)
                - 2*(mu - 1)*u[1]/rE
                + 2*(mu - 1)*u[1] * r * (r + 1) / rE^3
                - w[1] * (u[1]*u[2] - u[3]*u[4])
                - w[3] * (u[1]*u[4] - u[2]*u[3])
                - w[4] * (u[1]*u[3] + u[2]*u[4])
    dzeta[6] =  -C*u[2] 
                - (1/2)*w[1]*(mu - 1 + u[1]^2 + 3*u[2]^2 + u[3]^2 - u[4]^2)
                - 2*(mu - 1)*u[2]/rE
                + 2*(mu - 1)*u[2] * r * (r - 1) / rE^3
                + w[2] * (u[1]*u[2] - u[3]*u[4])
                + w[3] * (u[1]*u[3] + u[2]*u[4])
                - w[4] * (u[1]*u[4] - u[2]*u[3])
    dzeta[7] =  -C*u[3] 
                + (1/2)*w[4]*(mu - 1 - u[1]^2 + u[2]^2 + 3*u[3]^2 + u[4]^2)
                - 2*(mu - 1)*u[3]/rE
                + 2*(mu - 1)*u[3] * r * (r - 1) / rE^3
                + w[1] * (u[1]*u[4] - u[2]*u[3])
                - w[2] * (u[1]*u[3] + u[2]*u[4])
                + w[3] * (u[1]*u[2] - u[3]*u[4])
    dzeta[8] =  -C*u[4] 
                + (1/2)*w[3]*(mu - 1 - u[1]^2 + u[2]^2 - u[3]^2 - 3*u[4]^2)
                - 2*(mu - 1)*u[4]/rE
                + 2*(mu - 1)*u[4] * r * (r + 1) / rE^3
                + w[1] * (u[1]*u[3] + u[2]*u[4])
                + w[2] * (u[1]*u[4] - u[2]*u[3])
                - w[4] * (u[1]*u[2] - u[3]*u[4])
    
    return nothing
end