function KS_eom(dU, U, params, t)
    mu = params[1]
    C = params[2]

    u = U[1:4]
    w = U[5:8]

    r2_sq = (u[1]^2 - u[2]^2 - u[3]^2 + u[4]^2 + 1)^2 + 4*(u[1]*u[3] + u[2]*u[4])^2 + (2*u[1]*u[2] - 2*u[3]*u[4])^2
    norm_u2 = u[1]^2 + u[2]^2 + u[3]^2 + u[4]^2
    
    dU[1] = w[1]/4 + u[2]*(mu - 1 + u[1]^2 + u[2]^2 + u[3]^2 - u[4]^2)/2 - u[1]*u[3]*u[4]
    dU[2] = w[2]/4 + u[1]*(mu - 1 - u[1]^2 - u[2]^2 + u[3]^2 - u[4]^2)/2 + u[2]*u[3]*u[4]
    dU[3] = w[3]/4 + u[4]*(1 - mu + u[1]^2 - u[2]^2 + u[3]^2 + u[4]^2)/2 - u[1]*u[2]*u[3]
    dU[4] = w[4]/4 - u[3]*(mu - 1 - u[1]^2 + u[2]^2 + u[3]^2 + u[4]^2)/2 + u[1]*u[2]*u[4]

    dU[5] = - C*u[1]
            - w[2]*(mu + u[1]^2 + 3*u[2]^2 + u[3]^2 - u[4]^2 - 1)/2
            - 2*(mu - 1)*u[1] / sqrt(r2_sq)
            + 2*(mu - 1)*u[1]*norm_u2*(norm_u2 + 1) / r2_sq^(3/2)
            - w[1]*(u[1]*u[2] - u[3]*u[4])
            - w[3]*(u[1]*u[4] - u[2]*u[3])
            - w[4]*(u[1]*u[3] + u[2]*u[4])

    dU[6] = - C*u[2]
            - w[1]*(mu + u[1]^2 + 3*u[2]^2 + u[3]^2 - u[4]^2 - 1)/2
            - 2*(mu - 1)*u[2] / sqrt(r2_sq)
            + 2*(mu - 1)*u[2]*(norm_u2 - 1)*norm_u2 / r2_sq^(3/2)
            + w[2]*(u[1]*u[2] - u[3]*u[4])
            + w[3]*(u[1]*u[3] + u[2]*u[4])
            - w[4]*(u[1]*u[4] - u[2]*u[3])

    dU[7] = - C*u[3]
            + w[4]*(mu - u[1]^2 + u[2]^2 + 3*u[3]^2 + u[4]^2 - 1)/2
            - 2*(mu - 1)*u[3] / sqrt(r2_sq)
            + 2*(mu - 1)*u[3]*(norm_u2 - 1)*norm_u2 / r2_sq^(3/2)
            + w[1]*(u[1]*u[4] - u[2]*u[3])
            - w[2]*(u[1]*u[3] + u[2]*u[4])
            + w[3]*(u[1]*u[2] - u[3]*u[4])

    dU[8] = - C*u[4]
            + w[3]*(mu - u[1]^2 + u[2]^2 - u[3]^2 - 3*u[4]^2 - 1)/2
            - 2*(mu - 1)*u[4] / sqrt(r2_sq)
            + 2*(mu - 1)*u[4]*norm_u2*(norm_u2 + 1) / r2_sq^(3/2)
            + w[1]*(u[1]*u[3] + u[2]*u[4])
            + w[2]*(u[1]*u[4] - u[2]*u[3])
            - w[4]*(u[1]*u[2] - u[3]*u[4])
end